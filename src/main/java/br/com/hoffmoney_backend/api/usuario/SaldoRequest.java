package br.com.hoffmoney_backend.api.usuario;

public class SaldoRequest {
    private Long id;
    private Double valor;

    // Getters e Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Double getValor() {
        return valor;
    }

    public void setValor(Double valor) {
        this.valor = valor;
    }
}